<p>Bottle gourd is also known as Calabash or lauki and it belongs to family Cucurbitaceae. It is an annual climbing vine which has vigorous growth. The plant bears white color flowers which bears fleshy and bottle shaped fruits. The fruit is used for cooking purpose. There are also health benefits of bottle gourd. It helps in better digestion, reduces sugar level and constipation, cures insomnia and urinary infections and is good remedy for treating insomnia.</p>
