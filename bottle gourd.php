<button class="accordion">Sowing</button>
<div class="panel">
<p><b>Time of sowing</b><br>
The optimum time for sowing is February-March, June-July and November-December month.<br>
<b>Spacing</b><br>
Use row spacing of 2.0-2.5m and plant spacing of 45-60cm.<br> 
<b>Sowing Depth</b><br>
1-2cm deep seeds are sown.<br></p>
</div>
<br>
<button class="accordion">Seed</button>
<div class="panel">
<p><b>Seed Rate</b><br>
Seed rate of 2kg is sufficient for one acre land.<br>
<b>Seed Treatment</b><br>
Seed is treated with Bavistin@0.2% @3gm/kg to protect the seeds from soil borne fungus.</p>
</div>
<br>
<button class="accordion">Irrigation</button>
<div class="panel">
<p>Crop requires immediate irrigation. After sowing, immediate irrigation is given. In summer season 6-7 irrigations are required and in rainy season if needed then irrigation is given. In total, 9 irrigations are required.</p>
</div>
<br>
<button class="accordion">Weed Control</button>
<div class="panel">
<p>To control the weeds, 2-3 hoeings are required at the initial stages of plant growth. Weeding operations are carried out at the time of fertilizer application. Earthing up is also an effective way which should be carried out in rainy season. </p>
</div><br>
<button class="accordion">Plant Protect</button>
<div class ="panel">
<p><br><b> Pest and their Control</b><br>
<b>Fruit fly:</b> They feed themselves on internal tissues of fruits which cause premature fruit dropping and rotting and yellowing of fruits.<br>
<b>Treatment:</b> Spray with Carbaryl@10% @ 600-700gm in 150ltr water per acre.<br>
<b>Pumpkin beetles:</b> The beetles destroy the roots by feeding on themselves.<br>
<b>Treatment:</b> Spray with Carbaryl@10% @ 600-700gm in 150ltr water per acre.<br>
<b>Epilachna beetle:</b> The grubs destroy the plant parts by feeding on them.<br>
<b>Treatment:</b> Spray with Carbaryl@10% @ 600-700gm in 150ltr water per acre.<br>
<br><b>Disease and their control:</b><br><br>
<b>Powdery mildew:</b> Small, white color spots are seen on leaves and stems are the symptoms of this disease.<br>
<b>Treatment:</b> Spraying of M-45 or Z-78@400-500gm is used to get rid of this disease.<br>
<b>Downy mildew:</b> Chlorotic spots are the symptoms of this disease.<br>
<b>Treatment:</b> Spray with Mancozeb@400gm in 150ltr water per acre.<br>
<b>Mosaic:</b> The disease causes stunted growth and yield gets reduced.<br>
<b>Treatment:</b> Spray of Dimethoate@200-250ml in 150ltr water is done to get rid of mosaic.<br></p>
</div>
