<button class="accordion">Harvesting</button>
<div class="panel">
<p>Depending upon variety and season, crop is ready for harvesting in 60-70days. Depending upon market requirement, medium and tender fruits are harvested. Mature fruits are mostly stored for seed production purpose. Cut the fruits from vines with help of sharp knife. In peak season, picking should be done in every 3-4 days.</p>
</div><br>

<button class="accordion">Post Harvest</button>
<div class="panel">
<p>Keep isolation distance of 800m from other varieties of sponge gourd. Remove diseased plants from the field. For seed production, the fruits are harvested when they get physiologically mature. Three field inspections are required for the production of true to type seed. After harvesting, fruits are dried and then seeds are extracted.</p>
