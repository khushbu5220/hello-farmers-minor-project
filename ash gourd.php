<div class="panel">
<p><b>Time of sowing</b><br>
In north India, it can be cultivated for two times. It is sown in February-March and also in June -July.<br>
<b>Spacing</b><br>
Sow two seeds per hill on one side of bed which is 3 m wide and use spacing of 75-90 cm.<br> 
<b>Sowing Depth</b><br>
Seeds are sown at depth of 1-2 cm.<br>
<b>Method of sowing</b><br>
Seeds are directly sown on bed.</p>
