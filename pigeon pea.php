<button class="accordion">Planting</button>
<div class="panel">
<p><b>Time of sowing:</b>
Timely sowing of crop is important as delay in sowing leads to yield loss. Sow crop in second fortnight of May for obtaining high grain yield
<br>

<b>Spacing:</b>
For sowing use spacing of 50 cm between the rows while 25 cm between the plant.<br>
 
<b>Sowing Depth:</b>
Seed are sown with help of seed drill at depth of about 7-10 cm. 
<br>

<b>Method of Sowing:</b>
Seeds can be sown by broadcasting method but line sowing with help of seed drill is more efficient way of sowing for good yield.</p>
</div>
<br>
<button class="accordion">Caring</button>
<div class="panel">
<p><b>Fertilization:</b>Apply N:P:K @ 6:16:12kg/acre in form of Urea @ 13kg, DAP @ 35kg or SSP @ 100kg, and MOP @ 20kg/acre. Drill all fertilizer in soil at time of sowing. Apply fertilizer based on soil testing results. Also K should be apply when soil testing show deficiency of it. In DAP is to be used, do not apply Nitrogen fertilizer.<br><br>
<b>Watering:</b>Three to four week after sowing apply first irrigation. Remaining irrigations are depends upon rainfall intensity. Flower initiation and pod setting stages are the most crucial to drought stress. Therefore, irrigation at these stages is essential for good yield. Avoid excessive irrigation as it leads to more vegetative growth and incidence of Phytophthora and alternaria blight. Do not apply irrigation after mid-September; it will affect maturity of crop.
<br><br>
<b>Weeding:</b>Take one hoeing, about three weeks after sowing and another hoeing about six week after sowing. Apply Pendimethalin @ 1Ltr/acre in 150-200ltr of water as pre-emergence herbicide within 2 days after sowing, followed by hand weeding six to seven weeks after sowing. </p>
</div>
<br>
<button class="accordion">Pesticide and diesease</button>
<div class="panel">
<p><b>Pest and their control:</b><br>
i.<b>Blister beetles:</b>Also known as flower beetles, they feed on flower and thus reduces pod numbers. Adults are black beetles with bright red coloration on the forewings.
<br>To control it, spraying Deltamethrin 2.8EC @ 200ml or Indoxacarb 14.5SC @ 200ml per acre using 100-125 litres of water per acre. Take spray in evening hours and if necessary repeat the spray after 10 days.
<br>
ii.<b>Pod Borer:</b>This is the most serious pest and causes damage up to 75% reduction in yield. It feed on leaves causes skeletonization of leaves also feeds on flower and green pods. On pods they make circular holes and feed on grains.
<br>Install Pheromone traps for Helicoverpa armigera @ 12/ha. In case of low infestation, handpicked grown up larvae. At early stage use HNPV or Neem extract @ 50gm/Litre of water. Use of chemicals are necessary after ETL level. (ETL: 2 early instar larvae/plant or 5-8 eggs/plant).
<br>If incidence is observed, spray crop with Indoxacarb 14.5SC @ 200ml or Spinosad 45SC @ 60ml/100-125litres of water per acre using manually operated knapsack sprayer. Take spray in evening hour.
<br><br>
<b> Disease and their control:</b><br>
i.<b>Cercospora leaf spot:</b>Greyish brown to dark spots are observed under surface of leaves. In severe condition spots are observed on petioles and stems along with leaf drop.
<br>To control this disease, use disease free seeds and before sowing treat seeds with Captan or Thiram@3gm per kg seed.
<br>
ii.<b>Wilt:</b>This disease causes considerable loss in yield. It can affect at the seedling stage as well as in an advanced stage of plant growth. Initially affected plant show dropping of petioles and gives dull green color. Afterwards all leaves turn yellow and become straw colored.
<br>Grow resistant varieties. In primary stage of wilt, to control mix 1 kg of Trichoderma in 200 kg well decomposed cow dung and keep it for 3 days, then apply it in wilt affected area. If wilt is observed in fields, spray 300 ml Propiconazol with 200 Litre of water per acre.
<br>
iii.<b>Cankers:</b>  It causes due to various fungus. Cankers are developed on stem and twigs. It causes breaking of plants at affected place.
<br>Follow suitable crop rotation. In severe conditions, spray crop with Mancozeb 75WP @ 2gm per Litre of water.
<br>
iv.<b>Sterility Mosaic:</b>It is caused due to eriophyid mite. Due to infection of this disease, either no flowering or little flowering is observed. Leaves are of pale color. Plant gives bushy appearance.
<br>Grow resistant variety. For mite control Spray Fenazaquin 10% EC @ 300 ml/acre with 200 Ltr water.
<br>
v.<b>Phytophthora stem blight:</b>If it occurred at seedling stage, young seedling dies after emergence. Brown or black necrotic lesions are observed on stem. On leaflet, circular or irregular lesions are form and whole foliage becomes blighted.
<br>If Infestation Phytophthora blight observed, to control spray with Metalaxyl 8%+Mancozeb 64% @ 2gm/Ltr of water.
</p></div><br><br>
