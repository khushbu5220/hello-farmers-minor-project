<button class="accordion">Harvesting</button>
<div class="panel">
<p>For vegetable purpose harvest plant when leaves and pods are of green color. For grain purpose, when 75-80% pods turn brown and dry, it is right time of harvesting. Delay in harvesting result in damage of seeds. Harvesting can be done manually by cutting stem or by Machine. After harvesting keep bundles of plant upright for drying purpose. Grains are removed from plant by threshing or by traditionally i.e beating plants with sticks.</p>
</div><br>
<button class="accordion">Post-harvesting</button>
<div class="panel">
<p>Grains of harvested crop must be well dried before storage. And take care to avoid the pulse beetle infestation in storage.</p>
