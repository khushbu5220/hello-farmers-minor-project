<button class="accordion">Seed</button>
<div class="panel">
<p><b>Seed Rate</b><br>
Use 40,000 plants for planting in one acre land. Use only one plant for per hill. <br>
<b>Seed Treatment</b><br>
Seeds are treated with Carbendazim 50WP or Thiram @2gm/kg before sowing.</p>
</div>
<br>
<button class="accordion">Sowing</button>
<div class="panel">
<p><b>Time of sowing</b><br>
The optimum time for sowing beetroot is October to mid-November.<br>
<b>Spacing</b><br>
Use line spacing of 45-50cm for sowing. The plant to plant distance must be 15-20cm.<br> 
<b>Sowing Depth</b><br>
Sow the seed at depth of 2.5cm.<br>
<b>Method of sowing</b><br>
Dibbling method :Dibbling is the process of placing seeds in holes made in seedbed and covering them. In this method, seeds are placed in holes made at definite depth at fixed spacing. The equipment used for dibbling is called dibbler. It is a conical instrument used to make proper holes in the field. Small hand dibblers are made with several conical projections made in a frame. </p>
</div>
<br>

<button class="accordion">Irrigation</button>
<div class="panel">
<p>After sowing immediate irrigation should be given and then subsequent irrigation is done after two weeks of sowing. After that irrigation is required at the interval of 3-4weeks upto end February and at the interval of 10-15days during March-April month. Stop irrigation 2weeks before harvesting.</p>
</div><br>
<button class="accordion">Plant Protect</button>
<div class ="panel">
<p><br><b> Pest and their Control</b><br><br>
<b>Beet webworm:</b> If infestation is seen then spraying of Dimethoate 30EC @200ml/acre to get rid from webworm.<br>
<b>Weevil:</b> If infestation is seen then spraying of Methyl Parathion (2%) @2.5kg/acre is to be done to get rid of this pest.<br>
<b>Aphids and Jassids:</b> If infestation is seen then spraying of Chlorpyriphos 20EC @300ml/acre is to be done to get rid of aphids and jassids.<br>
<br><b>Disease and their control:</b><br><br>
<b>Alternaria and Cercospora leaf spot:</b> If infestation is seen then spraying of Mancozeb @400gm in 100-130ltr of water is done get rid from leaf spots.</p>
</div>
