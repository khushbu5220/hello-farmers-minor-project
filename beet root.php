<p>Beetroot is also known as garden beet. It is sweet in taste and is healthy having antioxidant properties. In the world after Sugarcane, Beetroot is the second largest sugar crop. It is a short duration crop which can be harvested in 6-7 months. It has medicinal values i.e. it is used to cure cancer and heart diseases. It can be grown easily and is ranked as top 10 vegetables grown in India. </p>
