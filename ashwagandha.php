<p>At the time of land preparation, about 4-8 tonnes of farm yard manure per acre should be mixed with soil and then the field is leveled with planking. After planking application of pata is done to level the field.  
 <br>There is no use of chemical fertilizers and pesticides as it is a medicinal plant and grows via organic farming. Some organic manure such as Farm Yard Manure (FYM), Vermi-Compost, Green Manure etc. is used as per requirement. Some bio-pesticides are prepared from neem, Chitrakmool, Dhatura, Cows urine etc. to prevent from soil or seed borne diseases. For higher production of fertile soil 6kg of Nitrogen (Urea@14kg) and 6kg of Phosphorus (SSP@38kg) per acre application is needed. Application of 40kg of N and P per ha are sufficient to produce high root yield in low fertile soils.
</p>
</div>
<br>
<button class="accordion">Irrigation</button>
<div class="panel">
<p>Excessive water or rain will harm the crops. If rain fall is heavy, then irrigation is not required otherwise once or twice irrigates the crops. Under irrigated conditions, the crop can be irrigated once in 10-15 days. First irrigation should be done after 30-35 days from germination and then second irrigation is done after 60-70 days from irrigation.</p>
</div><br>
<button class="accordion">Weed Control</button>
<div class="panel">
<p>Normally two weddings are done to keep the field free from weeds. One is done in about 20-25 days of sowing and then second is done after 20-25 days of first weeding. The dose of isoproturon 200g/acre and Glyphosate 600g/acre should be used before sowing the seeds to control the weeds.</p>
</div><br>
<button class="accordion"> Plant Protect</button>
<div class ="panel">
<p><b>Pest and their control:</b><br>
<b>Insect attack:</b>The Shoot borer and mite are the major insects. <br><br>
<b>Mite:</b> To control mite spray ethion 10ml/litre as soon as mites are noticed. <br><br>
<b>Shoot borer:</b>Shoot borer can be controlled with sumicidin at 10ml per litre.<br><br>
<b>Aphid:</b>It is  a small bug which feeds by sucking sap from plants; a blackfly or greenfly. They reproduce rapidly and cause extensive damage to plants. To get rid from aphids combination of 0.5% malathion and 0.1% - 0.3% kelthane as foliar spray at 10-15 days interval should be done. <br>
<br><b>Disease and their control:</b><br>
<b>Seedling rot and blight:</b>It is a disease that is caused by insects or nematodes which kill seeds or seedlings. To prevent from this use disease free seeds or Neem can be used.<br><br>
<b>Leaf spot:</b> It is a large number of fungal, bacterial, or viral plant diseases which cause leaves to develop discoloured spots. To prevent the crop from disease it should be sprayed with Dithane M-45 at the rate of 3g/litre of water, when 30 days old and the spray should be repeated at 15days interval if the diseases persist.<br><br>
</p>
</div>
<br>
<button class="accordion">Harvesting</button>
<div class="panel">
<p>Plant starts yielding 160-180 days. Harvesting is done in the dry weather when leaves are drying and berries change its color into red-orange. Harvesting is done by hands by uprooting the whole plant or through machines such as power tiller or country plough without damaging the roots.</p>
</div>
<br>
<button class="accordion">Post Harvesting</button>
<div class="panel">
<p>In post harvesting the roots are separated from the plant and cut into smaller pieces i.e. 8-10cm in length and then it is air dried. After post harvesting, grading is done. The root pieces are stored in tin containers for sale. The higher the length of root pieces the higher it will fetch the price. Berries are plucked separately and then they are air dried and crushed so as to take out the seeds. </p>
</div><br>
