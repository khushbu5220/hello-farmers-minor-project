<p>Ashwagandha is also known as wonder herbs as it have multiple medicinal property. It derives its named "Ashwagandha" as its roots smell like horse and it vitalize body. Its seeds, root and leaves are used for preparing various drugs. Drugs prepared from Ashwagandha used for stress reliever, to treat senile dysfunction also used to control anxiety, depression, phobia, schizophrenia etc. It is a branching shrub with average height of 30cm-120cm with fleshy, whitish brown roots. Flowers are greenish in color with orange-red berries. Rajasthan, Punjab, Haryana, Uttar Pradesh, Gujarat, Maharashtra and Madhya Pradesh are major growing states in India.
</p><br>
