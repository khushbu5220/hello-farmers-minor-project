<button class="accordion" style="width:75%">Soil</button>
<div class="panel">
  <p> Ashwagandha gives best result when grown in sandy loam or mild red soil with good drainage having pH in the range of 7.5 to 8.0. Growing Ashwagandha is not possible in soil that retains moisture and remains waterlogged. The soil should be loose, deep and well drained. Black or heavy soils having good drainage are also suitable for ashwagandha cultivation. </p>
</div>
<br>
<button class="accordion" style="width:75%">Land Preparation</button>
<div class="panel">
<p>For Ashwagandha plantation, it requires well pulverized and levelled soil. For fine tilth, plough field 2-3 times and ploughing or harrowing should be done before rains and then farm yard manure is applied. Land is prepared in month of April-May. </p>
</div>
<br>
<button class="accordion" style="width:75%">Sowing</button>
<div class="panel">
<p><b>Time of sowing</b><br>
For ashwagandha cultivation prepare nursery in the month of June-July.<br>
<b>Spacing</b><br>
Depending upon the growth habit and germination percentage, use spacing of 20 to 25 cm line to line distance 10cm plant to plant distance.<br> 
<b>Sowing Depth</b><br>
The seeds are usually sown about 1 to 3cm deep.<br>
<b>Method of Sowing</b><br>
Transplanting method is used of seedlings in main field.
<br></p>
</div>
<br>
<button class="accordion" style="width:75%">Seed</button>
<div class="panel">
<p><b>Seed Rate:</b>
For good varieties use seed rate of 4-5kg per acre.                   
<br>
<b>Seed treatment:</b>
To protect crop from seed borne disease and pest, before sowing do treatment with thiram or dithane M-45 (Indofil M-45) @3gms / kg of seeds. After treatment the seeds are then air dried and then use it for sowing.</p>
</div>
<br>
<button class="accordion">Nursery Management and Transplanting</button>
<div class="panel">
<p>Before sowing the land must be ploughed with mould board plough and harrowed twice to bring the soil in fine tilth and fill the soil with plenty of organic matter to nourish the soil. Treated seeds are sown on raised nursery bed from ground level.
<br>Before transplanting spray 10-20 tonnes of farmyard manure, 15kg of urea and 15kg phosphorus as a nutrient dose to the soil.
<br>The seeds germinate in 5-7 days and are ready for transplantation in about 35 days. Apply water appropriately before transplanting so that seedling can be easily uprooted. Transplanting should be done in the field with 40cm wide furrows.</p></div>
