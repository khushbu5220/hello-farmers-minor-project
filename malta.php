<button class="accordion"style="width:75%">Sowing</button>
<div class="panel">
<p><b>Time of sowing</b><br>
In Punjab planting is done in Spring season (February to March) and Monsoon season (15 August to October end).<br>
<b>Spacing</b><br>
For sweet oranges use spacing of 5mx5m is recommended. Dig pit of 1mx1mx1m, kept open in sun for some days. Add 15-20kg of well decomposed cow dung, SSP@500gm per Pit.<br> 
<b>Sowing Depth</b><br>
Pits of size 60×60×60cm should be dug for planting seedlings.<br>
<b>Method of Sowing</b><br>
<b>Propagation</b><br>
<b>Root stock raising: </b>Sow citrus seeds in nursery on bed of 2mx1m size and with 15cm distance between the row. When seedlings are of 10-12cm height, transplanting is to be done. Select healthy and uniform size seedling for transplantation purpose. Remove dwarf and weak seedlings. If needed, pruned roots slightly before planting. In nursery, Budding is done when plant are of pencil thickness. Shield budding or T shape budding is done for that, T shaped slot is made in bark of tree at distance of 15-20cm from ground. Horizontal cut of about 1.5-2cm long is given, and vertical cut of 2.5cm long is made from middle of horizontal cut. Remove bud from bud stick and insert it into T shape slots, afterward wrapped it with plastic paper.   
T budding is done during February-March and also in August- September. Propagation in sweet orange, kinnow, grape fruit is done by T budding while for lime and Lemon, it is propagated by Air layering method.
<br></p>
