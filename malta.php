<p>Malta is citrus fruit grown in India. It is commonly called as sangtra. It occupies nearly 30% of total area under citrus cultivation. Mandarin, Sweet Orange is commercially grown citrus species in India. Production of Mandarin orange is increasing every year in central and western parts of country. India ranks third in the production of orange after banana and mango fruits. In India, Orange is being grown in Rajasthan, Himachal Pradesh, Punjab and Uttar Pradesh.</p><br>
