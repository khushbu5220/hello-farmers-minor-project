<button class="accordion">Irrigation</button>
<div class="panel">
<p>Sangtra require irrigation at regular intervals. Lifesaving irrigation should be given in winters and summers. Irrigation is necessary for Flowering, Fruiting and proper plant growth. Over irrigation may also led to diseases like Root rot and collar rot. High frequency irrigation is beneficial. Salty water is injurious for crop plants. Partial drying out of soil in spring may not affect plants.</p>
</div><br>
<button class="accordion">Weed Control</button>
<div class="panel">
<p>Weed can be controlled by hand-hoeing and also controlled by chemically, use glyphosate@1.6litre per 150 litre of water. Use glyphosate only on weeds not on crop plants.</p>
