<button class="accordion"> Plant Protect</button>
<div class ="panel">
<p><b>Pest and their control:</b><br>
<b>Leaf miner:</b>Larvae inside the upper or lower surface of young and newly emerged leaves are curled and look distorted. Young trees show a reduction in growth due to leaf miner. Best management for leaf miner is to leave it alone and let the natural enemies to feed upon them and parasitize their larvae. It can also be controlled by spraying Phosphamidon  @1ml or Monocrotophos @1.5ml per 3-4 times fortnightly. Pheromone traps are also available for detecting leaf minor moths.<br>
<b>Citrus Psylla:</b>These are Juice sucking pests. Damage is mainly caused by Nymphs. It injects a plant toxin liquid which burns foliage and skin of fruit. Leaves curl and fall off prematurely. It can be controlled by pruning of diseased plants, burning them. Spraying of Monocrotophos-0.025% or carbaryl- 0.1% can also be helpful.<br>
<b>Scale Insects:</b>Citrus scale insects are small insects that suck sap from the citrus trees and fruits. Honeydew is produced which is feasted upon by ants. They do not have much mouth parts. Male citrus scale has a short life span. There are mainly two types of scale on citrus plants armored scale and soft scales.  Armored scale insects insert their mouth parts in the plant and never move again, eating and reproducing in the same spot. Soft scale bugs on citrus form a protective coating, which in turns cover citrus leaves and prevents photosynthesis. Once dead and soft scale will fall from tree instead of remaining stuck, they can be controlled with the introduction of indigenous parasitic wasps. Neem oil is also effective against them. Spraying of Parathion (0.03%) emulsion, dimethoate 150ml or malathion @0.1% are effective against scale control. <br>
<b>Aphids & Mealy Bugs:</b> They are small sap sucking pests. Bugs are present on the underside of leaves. Synthetic pyrethroids or pest oil can be used to control aphids and bugs.<br>
<br><b>Disease and their control:</b><br><br>
<b>Citrus Canker:</b>Plants have lesions on stems, leaves and fruits with brown, water-soaked margins. Citrus canker bacteria can enter through plant’s stomata into the leaves. Younger leaves are highly susceptible. Lesions oozes bacterial cells which can be dispersed by blowing wind to healthy plants in area.
<br>Contaminated equipment tends to spread disease to healthy plants. Bacteria can stay viable in old lesions for several months. It can be detected by appearance of lesions. It can be controlled by cutting of effected branches, twigs by spraying of Bordeaux mixture @1%. Aqueous solution of 550ppm, Streptomycin Sulphate is also helpful in controlling citrus canker.<br>
<b>Gummosis:</b>Exudation of gum from bark of tree is the characteristic symptom of gummosis disease. Affected plant leaves turns to pale yellow in color. Hardness masses of gum are common on stem and leaf surfaces. In severe cases, bark may be destroyed by rotting and tree may dies. Plant dies before the fruit matures. This disease is also called foot rot. This disease can be managed through proper selection of site with proper drainage, use of resistant varieties etc. Plant injuries should be avoided. Drench the soil with 0.2% metalaxyl MZ-72 + 0.5% trichoderma viride, which help to control this disease. Bordeaux mixture should be applied to plant upto 50-75 cm height from ground level at least once in a year.<br>
<b>Powdery mildew:</b>White cottony powdery growth is noticed on all aerial plant parts. Leaves tend to become pale yellow and crinkle. Distorted margins are also seen. Upper surface of leaves are more affected. Young fruits drop off prematurely. Yield is reduced significantly.  To control powdery mildew, affected plant parts should be removed and destroyed completely. Carbendazim, three times at 20-22 days of interval helps to control this disease.<br>
<b>Black Spot:</b>Black spot is a fungal disease. Circular, dark spots on fruits are seen. Copper spray in early spring should be sprayed on foliage helps to cure plants from black spots. It should be repeated in 6 weeks again. <br>
<b>Zinc Deficiency:</b>It is very common in citrus trees. It is notified as yellow areas between main lateral veins and midrib of the leaves. Twigs may die back, dense shoots having stunted bushy appearance is commonly seen. Fruits tend to become pale, elongated and small in size. Proper fertilizer application should be given to the citrus tree to prevent zinc deficiency. Zinc sulphate should be provided by dissolving 2 table spoons in 10 liters of water. This should be sprayed thoroughly on all the tree branches and foliage. It can also be cured by providing cow or sheep manure.<br>
<b>Iron deficiency:</b>Color of new leaves changes to yellowish green. Iron chelates should be provided to the plants. Cow or Sheep manure is also helpful to cure plants from iron deficiency. This deficiency mostly occurs in case of alkaline soils.<br>
</p>
