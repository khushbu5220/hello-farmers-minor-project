<div class="panel">
<p>Depending upon variety and season first picking is done after 60-80 days of sowing. Harvesting is done after 7 days of fruit setting. Picking should be done at the interval of 2-3 days.</p>
</div>
<br>
<button class="accordion">Seed Production</button>
<div class="panel">
<p>Keep isolation distance of 800m from other varieties of summer squash. Three inspections should be done, first is done before flowering, second is done at the time of flowering and fruiting development and third is done before harvesting of crop. Roughing of diseases and off type plant should be done. Seed harvesting is done when fruit turns from bright yellow to orange color. Harvested fruits are cut into two halves and then seeds are removed by hand. The seeds are then washed with water and the fruit flesh is poured off. The extracted seeds are then dried. It gives an average yield of 2-2.5qtl/acre.</p>
