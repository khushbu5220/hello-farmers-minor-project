<p><b>Pest and their control:</b><br>
i.<b>Powdery mildew:</b> The symptoms are appearance of white powdery spots on upper surface of leaves which causes leaf withering.<br>
<b>Treatment:</b> Application of carbendazim@2 gram in 1litre of water will help to cure powdery mildew. It can also be controlled by fungicidal sprays of Chlorothalonil, benomyl or dinocap.<br>
ii.<b>Aphid and Thrips:</b> They suck the sap from the leaves resulting in yellowing and drooping of leaves. Thrips results in curling of leaves, leaves become cup shaped or curved upward. <br>
<b>Treatment:</b> If infestation is observed in field, to control spray the crop with Thiamethoxam@5 gram/15 Litre of water.<br>
iii.<b>Flea beetle:</b> They are small shiny black beetles which feed on young plants. <br>
<b>Treatment:</b> To control flea beetle spray with Malathion 50 EC@1 ml or Dimethoate 30 EC@1 ml per one ltr of water.<br>
<br><b>Disease and their control:</b><br><br>
i.<b>Bacterial wilt:</b> It is caused by Erwinia tracheiphila. It affects the vascular tissues of the plant which result in immediate wilting.<br>
<b>Treatment:</b> Spraying of foliar insecticides is done to cure the bacterial wilt.<br>
