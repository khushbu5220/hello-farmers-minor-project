<button class="accordion"style="width:75%">Land Preparation</button>
<div class="panel">
<p>For summer squash farming, well prepared land is required. To bring the soil to fine tilth, 2-3 ploughing followed by harrowing is done. Furrow the field at 2m apart to a depth of 15cm. </p>
</div>
<br>
<button class="accordion"style="width:75%">Sowing</button>
<div class="panel">
<p><b>Time of sowing</b><br>
The optimum time for sowing is mid-January to March and October to November month (under protection).<br>
<b>Spacing</b><br>
Sow two seeds per hill at the distance of 45cm.<br>
<b>Sowing depth</b><br>
Seeds should be planted 2.5-3.5cm deep.<br>
<b>Method of Sowing</b><br>
Dibbling method</p>
</div>
<br>
<button class="accordion" style="width:75%">Seed</button>
<div class="panel">
<p><b>Seed Rate</b><br>
For summer squash farming, use seed rate of 2kg per acre.<br>
<b>Seed Treatment</b><br>
Before sowing, seed are soaked in water for 12-24 hours. It will increased germination percentage.<br>
To protect seed from soil borne fungus, treat seed with Carbendazim@2 gram/kg of seed or Thiram@2.5 gram/kg of seed before sowing. After chemical treatment, treat seed with Trichoderma Viride@4 gram/kg of seed or Pseudomonas fluroscens@10 gram/kg of seed.<br>
