<button class="accordion" style="width:75%">Soil</button>
<div class="panel">
  <p>It can be grown on wide variety of soils. Sandy loam to clay loam is considered to be most suitable soil for gram cultivation. Soil having water logging problem are not suitable for cultivation. Saline alkaline soil are not suitable. pH in the range of 5.5 to 7 is ideal for sowing.<br> 
Avoid sowing of same crop continuously in field. Follow proper crop rotation. Crop rotation with cereals help in controlling soil borne disease. Common rotation are Kharif fallow-chick pea, Kharif fallow- gram + wheat/barley/raya, Chari-gram, bajra-gram, rice/maize-gram.</p>
</div>
<br><br>
<button class="accordion" style="width:75%">Land Preparation</button>
<div class="panel">
  <p>Very fine and compact seedbed is not good for chick pea, it required rough seed bed. If it is cultivated as mix crop the land should be plough to fine tilth. If chick pea crop is taken after a kharif fellow carry out one deep ploughing during the monsoon as it will help to conserved rain water. Before sowing plough the land only once. If soil appears to be deficient in moisture run a roller about a week before sowing.</p>
</div>
