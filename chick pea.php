<p>Gram commonly known as chick pea or Bengal gram is the most important pulse crop of India. It is used for human consumption as well as for feeding to animals. Fresh green leaves are used as vegetable while straw of chickpea is an excellent fodder for cattle. The grains are also used as vegetable. India, Pakistan, Ethiopia, Burma and Turkey are main gram growing countries. India ranks first in the world in respect of production and acreage followed by Pakistan. In India, Madhya Pradesh, Rajasthan, Uttar Pradesh, Haryana, Maharashtra and Punjab are major gram producing states.
<br>On basis of size, color and shape of seeds, gram is divided into two group 1) Desi or brown gram 2) Kabuli or white gram. Yield potential of kabuli is poor as compared to Desi gram.</p>
