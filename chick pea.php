<button class="accordion"> Caring</button>
<div class="panel">
<p><b>Fertilization-</b>For Desi varieties for irrigated as well as unirrigated areas, apply Nitrogen in form of Urea@13 kg/acre and Phosphorus in form of Super Phosphate@50 kg/acre at time of sowing. Whereas for Kabuli varieties, apply Urea@13 kg/acre and Super Phosphate @100 kg/acre at time of sowing. For efficient use of fertilizer apply all the fertilizers are drilled in furrows at a depth of 7-10 cm.
<br><br><b>Irrigation-</b>
Where irrigation facilities are available, give a pre-sowing irrigation. It will ensure proper germination and smooth crop growth. After then give second irrigation at time of pre-flowering and one at pod development stage. In case of early rain delay irrigation and give according to requirement. Heavy and excess irrigation enhanced vegetative growth and depresses grain yield. Also it doesn’t tolerate water stagnation in field so provide proper drainage in field.<br><br>
<b>Weeding-</b>
To keep check on weeds, take first hand weeding or with wheel hoe 25-30days after sowing and second if needed after 60 days of sowing. Simultaneously for effective weed control, pre-emergence application of Pendimethalin @ 1Litre/200Litre water on third day after sowing for one acre land. It will help for controlling annual weeds. In case of less infestation, hand weeding or inter culture with the help of hoe is always better than herbicides because inter culture operations improve aeration in the soil.</p>
</div><br>

<button class="accordion">Pesticide and diesease</button>
<div class ="panel">
<p><b> Pest and their Control</b><br>
<b>Termite:</b> It feeds on root or near root zone of crop. Affected plant show symptom of drying up. It can be easily uprooted. It can affect at seedling stage and also near maturity.<br>
To protect seeds from termite, treat seeds with Chlorpyriphos 20EC@10ml/kg of seeds. If infestation occur in standing crop, drench with Imidacloprid@4ml/10litre of water or Chlorpyriphos@5ml/10Ltr of water.<br>
<br><br>
<b>Cut worm:</b> Caterpillar remain hide in soil at depth of 2-4 inch. It cut at base of plant, branches or stem. Eggs are laid down in soil. Larva is dark brown with red head.<br>
Adopt crop rotation. Use only well decomposed cow dung. At early stage pick caterpillar by hand and then destroyed them. Avoid plantation of Tomato. Okra near gram field. In low infestation spray Quinalphos 25EC@400 ml/200-240 litres water per acre. In case of severe infestation Spray with Profenophos 50EC@600 ml/acre in 200-240 litres of water.<br>
<b>Gram pod borer:</b> This is the most serious pest of chickpea and causes damage up to 75% reduction in yield. It feed on leaves causes skeletonization of leaves also feeds on flower and green pods. On pods they make circular holes and feed on grains.<br>
Install Pheromone traps for Helicoverpa armigera@5/acre. In case of low infestation, handpicked grown up larvae. At early stage use HNPV or Neem extract@50 gm/litre of water. Use of chemicals are necessary after ETL level. (ETL: 2 early instar larvae/plant or 5-8 eggs/plant).<br>
Spray Deltamethrin 1%+Triazophos35%@25 ml/10 litre water when crop is at 50% flowering stage. Spray Emamectin Benzoate 5%G@3 gm/10 litre of water 15 days after first spray of Deltamethrin+Triazophos.<br>
<br><b>Disease and their control:</b><br>
<b>Blight:</b> Dark brown spot with dot like bodies developed on stem, branches, leaflet and pods. In case of excessive rain whole plant get severely affected with blight.<br>
For cultivation use resistant varieties. Before sowing carryout seed treatment with fungicide. On incidence of disease spray with Indofil M-45 or Captan @360 gm/100 ltr of water per acre. If necessary repeat the spray at interval of 15 days.<br>
<br><b>Gray Mold:</b> Small water soaked spots are observed on leaflets. Spots on infected leaves become dark brown. In severe infestation, brown necrotic spots appear on twigs, petioles, leaves and flowers of the plant on attaining full vegetative growth. The affected stem finally breaks and the plant dies.<br>
Before sowing carry out seed treatment. If infestation is observed, spray crop with Carbendazim@2 gm/ltr of water.<br>
<br><b>Rust:</b> This disease is more severe in Punjab and Uttar Pradesh. Small, round to oval, light or dark brown pustules are formed on the under surface of the leaves. In later stage, pustules get black and affected leaves get defoliate.<br>
Use rust resistant varieties for cultivation. If symptoms are observed spray the crop with Mancozeb 75WP@2 gm/ltr of water. With interval of 10 days take two more sprays.<br>
<br><b>Wilt:</b> This disease causes considerable loss in yield. In can affect at the seedling stage as well as in an advanced stage of plant growth. Initially affected plant show dropping of petioles and gives dull green color. Afterwards all leaves turn yellow and become straw colored.<br>
Grow resistant varieties. In primary stage of wilt, to control mix 1 kg of Trichoderma in 200 kg well decomposed cow dung and keep it for 3 days, then apply it in wilt affected area. If wilt is observed in fields, spray 300 ml Propiconazol with 200 litre of water per acre.<br></p>
