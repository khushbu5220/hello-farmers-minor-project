<button class="accordion">Seed</button>
<div class="panel">
<p><b>Seed Rate</b><br>
Use seed rate of 15-18 kg/acre for desi variety and 37 kg/acre for Kabuli variety. Increased seed rate of desi gram to 27 kg/acre in case sowing is to done in second fortnight of November and 36 kg/acre in case sowing is to be done in first fortnight of December.<br>
<b>Seed Treatment</b><br>
Mix Trichoderma@2.5 kg/acre + decomposed cow dung@50 kg then cover it with jute bags for 24-72 hrs. Then spray this on moist soil before sowing to control soil borne disease. To prevent seeds from soil borne disease they should be treated with fungicide Carbendazim 12% + Mancozeb 63%WP(Saaf)@2 gm/kg of seed before sowing. In termite affected soil, treat seeds with Chlorpyrifos 20EC@10 ml/kg of seeds before sowing.<br>
Inoculate seed with Mesorhizobium, it will increased productivity of gram and increased yield by 7%. For that first moisten seed with water then apply single packet of Mesorhizobium on seeds. After inoculation dry seeds in shed.<br>
