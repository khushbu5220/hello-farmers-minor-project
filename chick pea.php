<button class="accordion">Sowing</button>
<div class="panel">
<p><b>Time of sowing</b><br>
For rainfed conditions, complete sowing from Oct 10 to Oct 25. Under irrigated condition complete sowing from October 25 to November 10 of desi and kabuli varieties. Sowing on right time is necessary as early sowing leads to excessive vegetative growth, also crop get affected due to wilt while late sowing, crop make poor vegetative growth and inadequate root development.<br>
<b>Spacing</b><br>
Seed should be spaced 10 cm apart allowing 30-40 cm between rows.<br> 
<b>Sowing Depth</b><br>
The seed should be placed 10-12.5 cm deep.<br>
<b>Method of sowing</b><br>
In north India, it is sown by pora method.</p>
</div>
