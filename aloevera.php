<button class="accordion" style="width:75%">Soil</button>
<div class="panel">
  <p> The plant can be grown in a variety of soils ranging from sandy coastal soils to loamy soils of plains. It cannot withstand in water logging conditions. It gives best results when grown under well drained loam to coarse sandy loam having pH ranges up to 8.5. </p>
</div>
<br>
<button class="accordion" style="width:75%">Land Preparation</button>
<div class="panel">
<p> Roots of Aloevera did not penetrate below 20-30 cm so depending upon soil type plough the land thoroughly and bring soil to fine tilth. At time of last ploughing add 6 ton per acre of well decomposed cow dung in soil. Forms ridge and furrow for planting of suckers at 45 or 60 cm apart. If necessary irrigate the field. Plant suckers at 40 or 30 cm apart.</p>
</div>
<br>
<button class="accordion" style="width:75%">Sowing</button>
<div class="panel">
<p><b>Time of sowing</b><br>
Plant suckers in July-August for better growth. Under irrigated conditions, sowing can be done around the year except in winter month.<br>
<b>Spacing</b><br>
Normally spacing of 45 cm x 40 cm or 60 cm x 30 cm is followed.
<br> 
<b>Sowing Depth</b><br>
Plant three to four month old suckers in a pit having depth of 15 cm.<br>
<b>Method of Sowing</b><br>
Parts used
<br>
Aloevera is obtained by cutting the leaves at their base and letting yellow, bitter Juice drain out. The water is evaporated off from the juice by heating and that result to light to dark brown mass.
<br></p>
</div>
