<button class="accordion">Irrigation</button>
<div class="panel">
<p>In summer or dry conditions, apply irrigation with interval of 2 weeks.  In rainy season, it does not require any irrigation and in winter season, less irrigation should be given as the plant not take up much water.  First irrigation must be done immediately after suckers get planted. Do not overwater the fields as they are harmful for crops. Remember that before watering the crops again let fields dry first. Before irrigation drenching should be done so that extra water will runs out.</p>
</div><br>
<button class="accordion">Weed Control</button>
<div class="panel">
<p>Do weeding and earthing up and keep field weed free. Weeding is to be done at proper intervals. Weeding is mainly done twice in a year.</p>
</div><br>
<button class="accordion"> Plant Protect</button>
<div class ="panel">
<p><b>Pest and their control:</b><br>
<b>Mealy Bug:</b> Caused by Lepidocephalus and Pseudococcus. The leaves start yellowing and withering.
<br>Application of Methyl parathion@10 ml or Quinalphos@20 ml in 10 Ltr of water is done on roots and shoots of the plant. <br>
<b>Disease and their control:</b><br>
<b>Black brown leaf spots:</b>Black Brown spots are characterized by reddish- brown spores that occur in oval or elongated pustules. The disease can develop rapidly when free moisture is available and temperatures are near 20°C. Successive generations of urediniospores can be produced every 10-14 days if conditions are favorable. <br>
<b>Anthracnose:</b>Dark brown or black spots are observed on shoots. On fruits also small, raise, dark spots are observed. Fruits rot completely within 2 to 3days after infection.
<br>Keep field clean, destroyed infected plant parts, fruits also avoid water logging condition in soil. After pruning take spray of Captan@300gm in 100Ltr water. At fruit set repeat the spray of Captan and continue till fruits get mature with interval of 10-15days. If infestation observed in field take spray of Copper oxychloride@30gm/10Ltr on infected trees.<br>
<br><br><br>
</p>
</div>
<br>
<button class="accordion">Harvesting</button>
<div class="panel">
<p>Aloevera crop take 18-24 months to fully mature. In year time, it bear yellow color flower. It can be harvested 4 times a year. 3-4 leaves cut from each plant. Carry out picking in morning or in evening. The leaves are regenerated and thus crop can be harvested up to 5 years.</p>
</div>
<br>
<button class="accordion">Post Harvesting</button>
<div class="panel">
<p> Allow freshly harvested plant to wilt and loose moisture in the field before transporting. Wilting is noticed normally within 24 to 72 hours. But the plant should be kept dry and cool to prevent fermentation or mould growth.  A concrete floor under shade can be used.</p>
