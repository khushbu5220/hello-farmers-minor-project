<button class="accordion">Seeding</button>
<div class="panel">
<p>Seeds of bajra are very small so for commercial planting it is propagated vegetative with help of stem cuttings (having two- three nodes) or root slips (approximately 30cm long). For planting one acre use 11,000 slips or stem cuttings. Under irrigated conditions, optimum time for planting is from last week of February to May. For rainfed areas, sowing can be done from June to August.
</p></div>
<br>
