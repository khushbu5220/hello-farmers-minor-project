<button class="accordion">Seeding</button>
<div class="panel">
<p><b>Seed Rate:</b>For sowing use seed rate of 38-40kg/acre.<br>

<b>Seed Treatment:</b>
Use healthy and well developed kernels for sowing. Discard very small, shrivelled and diseased kernels. Carry out seed treatment with Thiram@5gm or Captan@2-3 gm/kg or Mancozeb@4g/kg or Carboxin or Carbendazim@2gm/kg of the kernels to avoid infestation from ground borne disease. After chemical treatment, treat the seeds with Trichoderma viride@4g/kg seed or Pseudomonas fluorescens@10gm/kg seed. Seed treatment will protect the young seedlings from root-rot and collar rot infection.
</p></div>
<br>

<button class="accordion">Planting</button>
<div class="panel">
<p><b>Time of sowing:</b>
Sow the rainfed crop with the advent of monsoon in the last week of June or in the Ist week of July. Complete sowing as early as possible as delay sowing results in reduction in yield. Where irrigation facility is available sow kharif groundnut from April end to May end.  <br>

<b>Spacing:</b>
Spacing to be adopted is depends upon type of variety. i.e., for semi spreading variety (M 522) use spacing of 30cm between the rows and 22.5 cm between plants and for bunchy type varieties (SG 99, SG84) use spacing of 30x15 cm.<br>
 
<b>Sowing Depth:</b>
Healthy and well developed pods should be hand shelled with a suitable groundnut sheer about fornight before sowing. Pods are sown with help of seed drill at depth of 8-10 cm at a seed rate of 38-40 kg/acre.<br>

<b>Method of Sowing:</b>
seeds are sown with help of seed drill.</p>
</div>
<br>
<button class="accordion">Caring</button>
<div class="panel">
<p><b>Fertilization –</b>Apply fertilizer dose depending upon Soil testing. Due to this exact amount of fertilizer needed for soil is given and thus unnecessary loss of fertilizer is avoided. Apply Urea@13kg/acre, SSP@50kg/acre and depending upon soil testing if soil show deficiencies of potash then apply 10kg/acre of MoP. Also apply Gypsum@50kg/acre. Broadcast gypsum and drill all fertilizers at time of sowing. Application of gypsum encourages pod formation and better filling up of the pods.
<br>
The leaves of upper portion of crop get smaller and it gives light yellow appearance, this is due to Zinc deficiency. Crop growth gets stunted and the kernels are shrivelled in severe condition. Apply zinc sulphate heptahydrate@25kg or zinc sulphate monohydrate@16kg per acre. This dose will be sufficient for 2 to 3 years.
<br><br>
<b>Watering –</b>Irrigation for two or three time depending upon seasonal rainfall is necessary for good crop growth. Apply first irrigation at flowering stage. If the Kharif crop is caught in a long spell of draught, especially at the pod formation stage, supplemental irrigation is given, if water is available (In pod development stage, 2 - 3 irrigations are given depending on the soil type). Another irrigation a few days before the harvest may be given for full recovery for pods from soil.
<br><br>
<b>Weeding -</b>Weed control during first 45 days of growth period is essential for good yield. The most critical period is 3-6 weeks after sowing of crop. The average yield loss due to weed is about 30% whereas under poor management yield loss by weed may be 60%. Therefore carryout mechanical or chemical weed control during initial stage of crop growth.
<br>Give two hoeing after the first three weeks of sowing and then after second three weeks of sowing. No interculture would be done after the pegs have commmenced going underground. Apply Fluchoralin @ 600 ml per acre or Pendimethalin @ 1 ltr per acre as pre-emergence area followed by hand weeding once 36-40 days after planting.
<br>Accomplish earthing up during second hand weeding/late hand weeding (in herbicide application). It is an important operation in groundnut. Earthing up is to be done within 40-45 days after sowing as it helps for the penetration of pegs in the soil and also facilitates for increased pod development.</p>
</div>
<br>
<br>
<button class="accordion">Pesticide and diesease</button>
<div class="panel">
<p><b>Pest and their control:</b><br>
<b>White grub:</b> The adult beetles emerge from the soil during June-July with the first showers of rain. They congregate on the nearby trees such as ber, guava, rukmanjani, grapevines, almonds etc. and feed on their leaves during night. The eggs are laid in the soil and the larvae (grubs) hatching from them eat away the rootlets or root hairs of the groundnut plants.
<br>For effective management of white grub plough the field twice during May-June. It exposes the beetles resting in the soil. Do not delay crop sowing. Before sowing treat the seed with Chlorpyriphos 20E C@12.5 ml per kg kernels. For beetle control, spray with carbaryl @900 gm/100 Ltr of water. The spray should be repeated after every rainfall till the middle of July. Apply Phorate @ 4 kg or carbofuran @ 13 kg per acre in the soil at or before sowing.
<br>
<br>
<b>Hairy caterpillar:</b>The caterpillars occur in masses and defoliate the crop, reducing the yield. Larvae are reddish brown with black band and reddish hairs on all over body.
<br>Set up 3-4 light traps immediately after receipts of rains. Collect and destroy egg masses in the cropped area. Avoid migration of larvae by digging a trench 30 cm deep and 25 cm wide with perpendicular sides around the infested fields. Distribute small balls of poison bait in the field during evening hours. To prepare poison bait, mix 10 kg of rice bran, 1 kg jaggery and one litre Quinalphos. To control young larvae, do dusting of Carbaryl or Quinalphos at 300 ml/acre. To control grown up caterpillar, spray with 200 ml of Dichlorvos 100 EC @200 Ltr water/acre.
<br><br>
<b>Aphids:</b> Its infestation is high when rainfall is low. These are black bodied tiny insects suck the sap making the plants stunted and yellow. They secrete a sticky fluid (honeydew) on the plant, which is turned black by a fungus.
<br>It can be controlled by spraying of rogor @ 300ml/acre or Imidacloprid 17.8 % SL@ 80 ml/acre or Methyl demeton 25% EC @ 300 ml/acre as soon as the symptoms are seen.
<br><br>
<b>Groundnut leaf miner:</b>Young larvae boar into the leaflets and form small purple blotch on the leaf. Later stages larvae web the leaflets together and feed on them, remaining within the folds. Severely attacked field gives "burnt" appearance. Set up light traps @ 5/acre. Apply Dimethoate 30EC@300 ml/acre or Malathion 50 EC @400ml/acre or Methyl demeton 25% EC@ 200 ml/acre.
<br><br>
<b>Termites: </b>Termites penetrate and hollow out the tap root and stem thus kill the plant. Bore holes into pods and damage the seed. Due to infestation of termite wilting of plant is observed.
<br>Use well decomposed cow dung. Do not delay harvesting of crop. Seed treatment with chlorpyriphos @ 6.5ml /kg of seed may reduce termite damage. Do Dusting of Chlorpyriphos@2 ltr/acre in soil before sowing in endemic areas.
<br><br>
<b>Pod borer:</b>The holes are observed in young plant that is plugged with excreta. Nymph is of white in early stages and later turns brown.
<br>Apply Malathion 5D@10 kg/acre or Carbofuran 3%CG @13 kg/acre, on soil at 40 days prior to sowing on infected area.
<br><br>
<b> Disease and their control:</b><br>
<b>Tikka or Cercospora Leaf-spot:</b>Necrotic circular spot surrounded by a light-yellow ring on the upper side of the leaves.
<br>To control the disease take care from starting, from selection of seeds. Select healthy and unblemished kernels. Before sowing do seed treatment with Thiram (75%)@5 gm or Indofil M-45 (75%)@3 gm/kg of kernels. Spray the crop with Wettable sulphur 50 WP@ 500-750 gm/200-300 Litres of water per acre. Give 3 or 4 sprays at fortnight intervals, starting from the first week of August. Alternatively, spray the irrigated crop with Carbendazim(Bavistin/Derosal/Agrozim 50 WP@500gm/200 Ltr of water per acre. Give three sprays at fortnight intervals, starting when the crop is 40 days old.
<br><br>
<b>Collar-rot and Seed rot: </b>  These diseases are caused by Aspergillus niger. It causes rooting of the hypocotyl region, wilting and death of the seedlings. To control seed treatment is necessary. Treat the seed with Thiram or Captan 3 gm/kg of seed.
<br><br>
<b>Alternaria leaf disease:</b>Characterized by blighting of apical portions of leaflets which turn light to dark brown color. In the later stages of infection, blighted leaves curl inward and become brittle. Lesions produced by A. alternata are small, chlorotic, water soaked, that spread over the surface of the leaf.
<br>If infestation is observed, do foliar application of Mancozeb@3 gm/ Ltr or Copper oxychloride@3 gm/acre or Carbendazim@ 3 gm/ Ltr of water.
<br><br>
<b>Rust:</b> Pustules appear first on the lower surface of leaf. They may be formed on all aerial plant parts apart from flower and pegs. Severely infected leaves turn necrotic and desiccate but are attached to the plant.
After infestation is observed, take spray of Mancozeb@400 gm/acre or Chlorothalonil@400 gm/acre or Wettable sulphur@ 1000 gm/acre. If necessary take second spray with interval of 15 days.
</p></div><br>
