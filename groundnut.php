<p>Groundnut is raised in a sandy loam as well as well drained sandy clay loam soil. Deep well- drained soil with pH of 6.5 -7 as well as having great fertility are perfect for groundnut cultivation. Spanish as well as Runner varieties are beneficial for good quality soil compared to the Virginia forms. The lack of pods is often rich in heavier grounds. An excellent climatic condition for better germination of groundnut is 31° C. Heavy and stiff clays are unsuitable for groundnut cultivation as the pod development is hampered in these soils.</p>
