<button class="accordion" style="width:75%">Soil preparation</button>
<div class="panel">
<p>After harvesting of previous crop, plough the land twice and pulverised soil to obtained good tilth of soil. Give a third ploughing if necessary for rainfed crop, in the end of June or early July. Use harrow or tillers for cultivation purpose. When land is heavily infested with perennial weeds, very deep ploughing is needed. For irrigated crop, make beds of convenient size depending upon topography. Nature of irrigation source etc. Application of 5-7 ton/acre of chicken manure or 10 ton/acre of farm yard manure or well decomposed cow dung should be done 1 month before sowing. It helps to improve soil structure along with good plant growth.  </p>
