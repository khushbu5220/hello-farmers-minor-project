<button class="accordion">Deficiency and their Remedy</button>
<div class="panel">
<p><b>Potassium Deficiency:</b>
Leaves are not growing properly and grows in irregular shape. Mature leaves show pale yellow appearance and veins remain green.
<br>To overcome deficiency apply Muriate of Potash@16-20kg/acre.
<br><br>
<b>Calcium Deficiency:</b>
Mostly observed in light soils or alkaline soils. Plants not grow properly. Leaves show curling up.
<br>To overcome this deficiency apply Gypsum@200kg/acre at peg formation stage.
<br><br>
<b>Iron deficiency:</b>
Entire leaf get white or chlorotic.
<br>If deficiency is observed, spray crop with ferrous sulphate@5gm + citric acid@1gm/Ltr of water with one week interval. Continue spraying till deficiency get disappeared.
<br><br>
<b>Zinc Deficiency:</b>
Affected plant shows leaves in cluster form, leaves growth get stunted and appear small.
<br>Take spray of Zinc sulphate@2gm/Litre of water. Spray for two-three times with 7 days interval.
<br><br>
<b>Sulphur Deficiency:</b>
Young Plant growth get stunted and appeared smaller in size. Also leaves are small and give yellowish appearance. Maturity of plant get delayed.
<br>As a preventive measure apply Gypsum@200kg/acre at planting and pegging stage.
</div>

<br>
<button class="accordion">Harvesting</button>
<div class="panel">
<p>Kharif sown crop is get ready to harvest in month of November. When plant show uniform yellowing of crop along with shedding of older leaves. Crop sown during April end - May end is ready for harvesting after the monsoons are over towards end of August and September. For efficient harvesting of crop, adequate moisture should be present in soil and crop should not be overripe. The tractor- mounted groundnut-digger shaker developed at the Punjab Agricultural University may be used for quick harvesting. Harvested plants are stacked for a few days for drying and stripped afterwards. After curing, collect the crop at one place and give 2-3 shakings and beatings daily for 2 to 3 days with a toothed rake or trangli to separate pods and leaves from the stalk. Collect the pods and leaves into a heap and winnow. Dried pods in sun for 4 or 5 days before storage.
On cloudy days removed pods and then immediately place them in an air drier@27-38 degree centigrade for 2 days or until pods dry to constant mass (6-8%).
</p>
</div>
<br>
<button class="accordion">Post-harvesting</button>
<div class="panel">
<p>After cleaning and grading, store pods in gunny bags and stack them up to 10 bags high in separated stocks so that air freely circulate among them. The bags should be piled on wooden plank to avoid damage due to dampness.
