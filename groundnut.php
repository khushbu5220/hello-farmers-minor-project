<p>Groundnut is a significant oilseed, ideal for cultivation in tropic regions of the country. Groundnut (Arachis hypogaea), is a species in the legume or "bean" family. It is considered to be local in South America. These are known by many other local names such as earthnuts, ground nuts, goober peas, monkey nuts, pygmy nuts and peanuts. Despite its name and appearance, the peanut is not a nut, but rather a legume.
<br>Groundnut is the third most important oilseed of world. In India, it is available throughout the year. It is important source of protein grown mostly under rain-fed conditions. In India, Uttar Pradesh, Madhya Pradesh, Rajasthan, Gujarat, Maharashtra, Karnataka, Andhra Pradesh and Tamil Nadu are major groundnut growing states.</p>
