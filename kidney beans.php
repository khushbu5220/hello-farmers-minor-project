<button class="accordion">Irrigation</button>
<div class="panel">
<p>For better germination of seed give pre-sowing irrigation. 6-7 irrigations are required during growing season. Irrigation on 25th day after sowing and three irrigation at 25 days interval are necessary to get optimum yield. Give irrigation prior to blooming, during flowering and at pod development stage, water stress at these stage will lead to yield loss.</p>
</div><br>
<button class="accordion">Weed Control</button>
<div class="panel">
<p>Initial growth period is crucial for crop growth. Avoid weed infestation at this stage. Complete weeding operations synchronized along with fertilizer and irrigation operations. Use Fluchloralin@ 800ml/acre or Pendimethalin@1ltr per acre as pre-emergence weedicide. </p>
</div><br>
<button class="accordion"> Plant Protect</button>
<div class ="panel">
<p><b>Pest and their control:</b><br>
<b>Thrips:</b>Commonly observed pest. Mostly observed in dry weather. They suck sap from the foliage and results in curling of leaves. Also causes flower drop. To check severity of thrips incidence, keep blue sticky traps @6-8 per acre. Also to reduce the incidence spray Verticillium lecani@5gm/Ltr water
<br> If incidence of thrips is more, then take spray of Imidacloprid 17.8SL or Fipronil @1ml/Ltr water or Acephate 75% WP@1gm/Ltr.<br>
<b>Mite:</b>These are widely distributed pest observed throughout the world. Nymphs and adults feed exclusively on the lower surface of the leaves. Infected leaves gives cup shape appearance. Heavy infestation results in defoliation, bud shedding and drying of leaves.
<br>If Infestation of yellow mite is observed in field, spray of Chlorfenapyr@15ml/Ltr, Abamectin@15ml/Ltr are found effective. Mites is a serious pest and it may cause yield loss up to 80%. For effective control spray Spiromesifen 22.9SC @200ml/acre/180Ltr of water. <br>
<b>Aphids:</b>They suck sap from the leaf. They excrete honey like substance and developed sooty mould i.e blackish colour fungus on the Calyx and pods thus deteriorate quality of product.
<br>To control take spray of Acephate 75SP@1gm/Ltr or Methyl demeton 25EC@2ml/Ltr of water. Soil application of granular insecticides viz Carbofuran, Phorate@4-8kg/acre on 15 and 60 days after transplanting were also effective. <br>
<br><b>Disease and their control:</b><br><br>
<b>Powdery Mildew:</b>Patchy, White powdery growth appear on lower side of leaves. It parasitizes the plant using it as a food source. It can developed at any stage of crop development. In severe infestation it causes defoliation.
<br>Avoid water lodging in field. Keep field clean. To Control spray with Hexaconazole along with sticker@1ml/Ltr of water. In case of sudden rain, chances of powdery mildew. Mild infestation take spray of water soluble Sulphur@ 20gm/10Ltr of water 2-3 times with interval of 10 days. <br>
<b>Wilt:</b>Moist and poorly drain soil causes damping off disease. It is soil borne disease. Water soaking and shrivelling of stem occurs. Seedlings killed before emergence.  
<br>To control Wilt, Drench nearby soil with Copper oxychloride@25gm or Carbendazim@20gm/10Ltr of water. To control Wilting of plants due to root rot do drenching with Trichoderma bio fungus@2.5kg/500Ltr water, near to roots of plants. <br>
<b>Yellow Mosaic:</b>Light and green patches observed on leaves. In early stage plant growth get stops. Yellowing, chlorotic ring spots on leaves and fruits. Select healthy and disease free seeds for cultivation. Uproot and destroyed infected plant away from field.
<br>If observed in field to control it take spray of Acephate 75SP@600gm/200Ltr or Methyl demeton 25EC@2ml/Ltr of water. <br></p>
</div>
