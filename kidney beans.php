<button class="accordion">Harvesting</button>
<div class="panel">
<p>Harvest when pods are full grown and ripe and there color turn to yellow. Also leaves turn yellow and majority of leaves drop. Depending upon variety use pods are ready to harvest 7-12 days after flowering. Overall crop is ready to harvest in 120-130 days. Do harvesting at right time as delay cause shattering. Keep harvested plant for three-four days in sun. After proper drying of crop, threshing is done with help of bullocks or with sticks.</p>
</div>
<br>
<button class="accordion">Post Harvesting</button>
<div class="panel">
<p>Kidney beans required little processing after harvesting but take care during storage to maintain good quality. Before storing, do sorting and remove damaged, infected beans. Heat and humidity cause deterioration in quality so always stored beans in cool, dark and dry place. </p>
