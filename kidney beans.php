<p>Kidney beans is also known as the chilli bean because of its dark red color and the visually resemblance the shape of a kidney. Kidney beans are a good source of protein also it is excellent source of molybdenum. It contain good source of cholesterol-lowering fibre. Rajma is a popular dish from the North Indian cuisine made from red kidney beans. Maharashtra, Jammu and Kashmir, Himachal Pradesh, Uttarakhand, West Bengal, Uttar Pradesh, Tamil Nadu, Kerala and Karnataka are major kidney bean growing states in India.</p><br>
<div style="float:right" class="zoom"><img alt="kidney beans" src="kidney1.jpg"></div>
<button class="accordion" style="width:75%">Soil</button>
<div class="panel">
  <p>It can be grown on wide range of soils from light sandy to heavy clay soils. Well drained loamy soil is good for kidney beans cultivation. It is very sensitive to saline soils. Gives best result when pH of soil is 5.5 to 6. </p>
</div>
<br>
<button class="accordion"style="width:75%">Land Preparation</button>
<div class="panel">
<p> Give two to three ploughing to bring soil at fine tilth. Make field level so that water stagnation should not occurred in main field. Crop is very sensitive to water logging. At last ploughing apply Farmyard Manure or well decomposed cow dung@60-80qtl/acre.</p>
</div>
<br>
<button class="accordion"style="width:75%">Sowing</button>
<div class="panel">
<p><b>Time of sowing</b><br>
For spring season, best time for Kidney beans cultivation is February-March and for Kharif season, it is sown during May-June month. In Punjab, some farmers sow Kidney beans in last week of January.  <br>
<b>Spacing</b><br>
For early sown varieties use spacing of 45-60 cm between rows and 10-15 cm between plants. For pole type varieties sown at distance of 1 m in hill@3-4plant per hill.<br> 
<b>Sowing Depth</b><br>
Sow the seeds at depth of 6-7 cm.<br>
<b>Method of Sowing</b><br>
For sowing dibbling method used. In plain area seeds are sown in line or on bed where as in hilly areas, seeds are sown on ridge.
<br></p>
</div>
<br>
<button class="accordion" style="width:75%">Seed</button>
<div class="panel">
<p><b>Seed Rate</b><br>
For early sown varieties use seed rate of 30-35kg/acre. For pole type varieties sown at distance of 1m in hill@3-4plant per hill with seed rate of 10-12kg/acre.<br>
<b>Seed Treatment</b><br>
Before sowing treat seeds with Thiram@4gm per Kg of seeds. Dried seed in shade and then sown immediately.<br></p>
</div>
