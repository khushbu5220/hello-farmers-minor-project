<p>Another name of Bitter gourd is bitter melon and is also known as Karela in Hindi. The botanical name of bitter gourd is Memordica charantia and it belongs to the family of Cucurbitaceae. It is popularly known for its medicinal, nutritional and other excellent health benefits. Because of its high demand in the market, bitter gourd farming is done very successful. Bitter gourd is mainly used for making juice and for culinary purpose. It is a rich source of Vitamin B1, B2 and B3, C, beta-carotene, zinc, iron, phosphorus, potassium, manganese, folate and calcium. It has health benefits such as it helps in preventing blood disorders, detoxifies blood and liver, boosts immune system and helps in weight control.   </p>
