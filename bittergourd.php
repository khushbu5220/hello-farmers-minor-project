<button class="accordion" style="width:75%">Soil</button>
<div class="panel">
  <p>Sandy loam soil which is rich in organic content and has good drainage system is ideal for the bitter gourd cultivation. The pH of the soil ranging from 6.5-7.5 is best for bitter gourd farming.</p>
</div>
<br>
<button class="accordion" style="width:75%">Land Preparation</button>
<div class="panel">
  <p>For bitter gourd farming, well prepared land is required. To bring the soil to fine tilth, 2-3 ploughings should be done followed by weeding. </p>
</div><br>

