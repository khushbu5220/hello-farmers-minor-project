<button class="accordion">Sowing</button>
<div class="panel">
<p><b>Time of sowing</b><br>
February to March or June to July is an optimum time for seed sowing.<br>
<b>Spacing</b><br>
Sow seeds on both side of 1.5 m wide beds and use plant to plant spacing of 45 cm.<br> 
<b>Sowing Depth</b><br>
2.5-3 cm deep seeds are sown in the pit.<br>
<b>Method of sowing</b><br>
Dibbling method :Dibbling is the process of placing seeds in holes made in seedbed and covering them. In this method, seeds are placed in holes made at definite depth at fixed spacing. The equipment used for dibbling is called dibbler. It is a conical instrument used to make proper holes in the field. Small hand dibblers are made with several conical projections made in a frame. </p>
</div>
<br>
<button class="accordion">Seed</button>
<div class="panel">
<p><b>Seed Rate</b><br>
Use seed rate of 2.0 kg per acre.<br>
<b>Seed Treatment</b><br>
Before sowing, seeds are soaked in 25-50 ppm Gibberelic acid and 25 ppm boron for 24 hours.</p>
</div>
<br>
<button class="accordion">Seed Production</button>
<div class="panel">
<p>Rouging should be done three times i.e. first at vegetative phase, second at flowering stage and third at the time of fruiting stage. Keep isolation distance of 1000m from other varieties of bitter gourd. Remove diseased plants from the field. Place one honey bee colony for the production of good seed yield. For seed production, the fruits are harvested when the fruit turns dark yellow to orange in color. After harvesting seeds are taken out form pulp, then they are cleaned and are shade dried.</p>
</div><br>
<button class="accordion">Irrigation</button>
<div class="panel">
<p>After sowing, first irrigation should be given. In summer season, irrigation is given after every 6-7 days and in rainy season, irrigation is given only when needed. In total, 8-9 irrigations are required. </p>
</div><br>
<button class="accordion">Weed Control</button>
<div class="panel">
<p>Intercultural operations are carried out for weed control. To control the weeds, 2-3 hoeing should be done in initial stage of plant growth. Weeding operations should be carried out in the soil at the time of fertilizer application and mainly earthing up is done during rainy season. </p>
</div><br>
<button class="accordion"> Plant Protect</button>
<div class ="panel">
<p><br><b> Pest and their Control</b><br><br>
<b>Aphids:</b> They suck the sap from the leaves resulting in yellowing and drooping of leaves.<br> 
Spraying of imidachloprid@0.5ml/ltr is done to control the aphids.<br>
<b>Mites:</b> Thrips results in curling of leaves, leaves become cup shaped or curved upward.<br> 
Spraying of Dicofol 18.5% SC@2.5ml/ltr water is done to control mites.<br>
<b>Beetles:</b>The symptoms are damage to flowers, leaves and stems.<br> 
Spraying of Malathion 50EC@1ml/ltr is done to control beetles. <br>
<br><b>Disease and their control:</b><br><br>
<b>Powdery mildew:</b> The symptoms are appearance of white powdery spots on upper surface of leaves which causes leaf withering.<br>
 Spraying of Carbendazim@3gm /ltr is done to control the powdery mildew.<br>
<b>Downy mildew:</b> If infestation is observed spraying of Mancozeb or Chlorothalonil@2gm/ltr is done twice at the interval of 10-12 days.</p>
</div>
