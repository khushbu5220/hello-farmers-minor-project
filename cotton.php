<b>Anthracnose:</b>small, reddish or light colored diseased spots are observed on plant leaves. Wounds are form on stem and thus plant get weaken. It attack boll at any stage and infection spread to lint and seed. The bolls affected by the disease have small, water-soaked, circular, slightly depressed, reddish brown spots.
<br>Before sowing, treat seeds with Captan or Carbendazim @3-4 gm/kg of seeds. Avoid water logging in field. If infection observed, remove affected plant and destroyed them away from field and take Spraying with Carbendazim @1 gm/lit of water.
