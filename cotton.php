<b>Root rot:</b>Sudden and complete wilting of plants. Leaves give yellow appearance. Affected plants can be easily pulled out. Except the tap root, few secondary roots are fresh which holds the plant and other roots are decayed.
<br>Before sowing apply neem cake@60 kg/acre to the soil. Treat seeds with T. viride @ 4 gm/kg of seeds to reduce root rot incidence. If infection is observed do spot drenching of Carbendazim@1 gm/lit at the base of affected plants as well as surrounding healthy plants.
