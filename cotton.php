<button class="accordion">Seeding</button>
<div class="panel">
<p><b>Seed Rate:</b>Seed rate varies with variety, growing zones, irrigation etc. Use seed rate for American cotton hybrid@1.5 Kg/acre while for American cotton variety use seed rate of 3.5 kg/acre. For desi cotton hybrid@1.25 kg/acre and 3 kg/acre for desi cotton varieties.</p>
</div>
<br>
<button class="accordion">Planting</button>
<div class="panel">
<p><b>Time of sowing:</b>
Optimum time for sowing is in April - mid May. For Management of Mealybug, sow Bajra, Arhar, Maize and Jowar in the fields surrounding cotton crop. Avoid growing tur, moong and bhendi in and around cotton field as these harbour insect pests. In Punjab cotton wheat rotation is common but rotation with berseem and clusterbean has been found to have beneficial effect on the succeeding cotton crop.<br>

<b>Spacing:</b>
For American cotton use spacing of 75x15 cm under irrigated condition while under rain fed condition use spacing of 60x30 cm. For desi cotton use spacing of 60x30 cm for rain fed as well as for irrigated condition.
<br>
 
<b>Sowing Depth:</b>
Sowing should be done at depth of 5 cm.<br>

<b>Method of Sowing:</b>
For sowing use seed drill for desi cotton while dibbling of seed is done in case of hybrids and Bt cotton. Square planting is beneficial compared to rectangular planting. Few gaps arise due to failure of seed germination and mortality of seedling. To overcome this gap filling is necessary. Two weeks after sowing the weak/diseased/damaged seedlings should be removed by keeping a healthy seedling/hill.</p>
</div>
<br>
<button class="accordion">Caring</button>
<div class="panel">
<p>The wheat plants are quite tough and strong. They generally do well in favorable climate conditions and require less care. And if you take some extra care, it will be good and will ensure maximum production.
<br>
<br>
<b>Fertilization –</b>Judicious use of fertilizers, irrigation and clean cultivation will prevent early build-up of pests and helps to conserve natural enemies of pests. To enhance subsidiary growth to have maximum number of boll bearing branches, cut top of growing point of main branch at around 5 feet height. At the time of last ploughing apply well decomposed cow dung @ 5-10 ton for rainfed and 10-20 ton per acre for irrigated soil. It will help to conserved moisture in soil. Fertilizer doses for Cotton varieties, apply Urea@65 kg and DAP@27 kg or SSP@75 kg per acre. And in case of hybrids, apply 130 kg/acre of Urea and DAP@27 kg or SSP@75 kg/acre. If DAP@27 kg is used instead of SSP then reduced Urea dose by 10 kg.
 <br>
Apply whole dose of phosphorus in last ploughing. Apply half of Nitrogen fertilizer at time of thinning and remaining half at the appearance of first flower. For low fertile soil, apply half dose of Nitrogen at time of sowing. To reduce nitrogen loss from Urea mix 8 kg sulphur powder/ 50 kg Urea and apply in between rows of standing crop.
WSF: 80-100 days after sowing, if no or poor flowering is observed then to enhance flowering spray with multi micronutrient fertilizer@750 gm/acre/150 Ltr of water. In case bt varieties to improve yield, spray with 13:0:45@10 gm or Potash@5 gm/Ltr of water at evening time on 85, 95 & 105th days after sowing. Also to get higher yield, take alternate sprays of Potassium@10 gm/Ltr and DAP@20 gm/Ltr (2-3 sprays each at 15 days interval from first blooming). Sometime flower and square drop is observed then to control flower drop and to obtain good yield spray with Planofix(NAA) @4 ml and Multi Micronutrient fertilizer@120 gm, Magnesium Sulphate@150 gm/15 Ltr water. Due to bad weather effect boll drop is seen then to control, spray 00:52:34@100 gm+Humic acid(>12%) @30 ml+Sticker @6 ml in 15 Ltr water thrice at 10 days interval. Leaf reddening is deficiency observed now a day. It cause mainly due to lack of nutrient management. It can be corrected by proper fertilizer management. For that take Foliar spray of MgSO4@1 kg, followed by Urea@2 kg in 100 Ltr water.
<br><br>
<b>Watering –</b>Cotton required four to six irrigation depending upon rainfall intensity. Give first Irrigation to the crop four to six week after sowing. And remaining irrigation at interval of two or three weeks. Never let water to stand in younger plants. Do not let the crop to suffer for want of water during flowering and fruiting to avoid the shedding of flowers and bolls. Give last irrigation to the crop when 33% of bolls are opened and after that there is no need of more water through irrigation.
<br>Whenever salty water has to use for irrigation, the water should be get tested from authenticated laboratory and as per their recommendation, Gypsum or Pyrite may be added. In Drought Condition, alternate furrow irrigation and use of micro irrigation system (wherever feasible) will be of immense help in saving irrigation water.
<br><br>
<b>Weeding -</b>Due to wide spaced crop weeds pose serious threat. A weed free period of 50-60 days from sowing is necessary for good yield otherwise it may cause 60-80% reduction in yields. Manual, mechanical and chemical methods of weed control in combination is necessary for effective weed control. Carry out first manual hoeing 5-6 weeks after sowing or before first irrigation. Remaining hoeing should be done after each irrigation. Do not allow congress grass to grow around cotton fields, as they increase the possibility of mealy bug infestation.
<br>To control weeds after sowing but before its emergence take spray of Pendimethalin @ 25-33 ml/10 Ltr of water. Apply Paraquat (Gramoxone) 24% WSC @ 500 ml/acre or Glyphosate @1 Ltr/acre in 100 Ltr of water, 6 to 8 week after sowing when crop is 40-45 cm in height. Crop is highly sensitive to the 2,4-D weedicide. Its vapours can cause serious injury to cotton even if sprayed in adjoining fields. Spraying of herbicide should be carried out either in morning hour or in evening hours.
</p></div>
<br>
<button class="accordion">Pesticide and diesease</button>
<div class="panel">
<p><b>Pest and their control:</b><br>
<b>American Bollworm:</b>Eggs are laid singly on both upper and lower leaf surfaces. Newly hatched larva is yellowish white with brown black head. Later, body color changes to darker and after that changes to brownish. Due to infestation of this pest, circular holes are observed on boll. Presence of granular faecal pellets outside the bore hole. Single larva can damage 30-40 bolls. To check infestation use light traps, pheromone traps.
 <br>Avoid continuous cropping of cotton. Avoid mono-cropping. Growing of less preferred crops like green gram, black gram, soybean, castor, sorghum etc., along with the cotton as inter-crop or border crop to reduce the pest infestation. Before sowing of cotton, remove crop residue of previous crops. Use optimum quantity of water and avoid excess use of nitrogen fertilizer. Use resistive varieties. Don’t use synthetic Pyrethroids to control American Bollworm. In case of severe infection Spray any one from Cypermethrin @ 1 ml/Ltr of water or Deltamethrin @1 ml/Ltr of water or Fenvalerate @1 ml/Ltr or Lambda cyhalothrin @1 ml/Ltr of water on affected crops, depending upon availability. For effective control of boll worms, never spray pesticides of single group more than once.
<br>
<br>
<b>Pink boll worm:</b>Young larva is of white color and late instar is vary in color ranging from almost black, brown or green to pale or pink. To monitor adult moths activity, use pheromone trap. If infestation is observed do dusting of Carbaryl 5%D@10 kg/acre. In case of severe infestation, spray with Triazophos 40 EC@300 ml/acre.
<br><br>
<b>Tobacco Caterpillar:</b> It attacks on crop in group and Scrapping the epidermal layer, leaving the skeleton of veins of leaf. During severe attack only stem and side shoots will be standing. Eggs of tobacco caterpillar are of golden brown and appears in masses. Larva is of greenish color.
 <br>Use light trap to know intensity of pest. Set up the sex pheromone trap at 5/acre. Do manual inspection for pest. Larva are found in cluster, collect and destroyed early stage larva also grown up caterpillar. In case of high infestation spray any one of the insecticides, Chlorpyriphos 20 EC@1 Ltr/acre or Chlorantraniliprole 18.5% SC@50 ml/acre or Diflubenzuron 25%WP@100-150 gm/acre. Spraying of insecticides should be done either in the early morning or in the evening.
 <br><br>
<b>Jassids:</b> Nymphs and adults of jassids suck sap from underside leaves and causing of curling. Leaves turn red or brown then dry up and shed. Apply Carbofuran 3 G@14 kg or Phorate 10 G@5 kg/acre near to root zone in moist soil. When upper canopy show yellowing and curling of leaves on 50% of plants, take spraying of insecticides. Spray Imidacloprid 17.8 SL@40-50 ml or Thiamethoxam @40 gram or Acetamiprid @75 gm/acre/200 liters of water.
<br><br>
<b>Spotted Bollworm:</b>Larva is of dull green, black bristles color and having lines of black spots on body. Due to incidence of bollworm, drying and drooping of terminal shoots during pre–flowering stage is observed. It create holes on boll and then causes rotting of boll.
<br>If infestation is observed, to control spray with Profenophos 50 EC@500 ml/200 Ltr of water/acre.
<br><br>
<b>Mealy bugs:</b>Found under surface of leaves in clusters and secrete wax like substance. Due to honey dew like secretion, sooty mould is developed and affected plant gives sick, black appearance.
 <br>Grow Maize, bajra and jawar as barrier crops. Do not throw mealy bug infested uprooted plants in water channels or vacant spaces, instead burn them. Do not allow congress grass to grow around cotton fields, as they increase the possibility of attack by mealy bug. To control spread of mealy bugs in newer areas, avoid movement of human beings or animals from infected area to healthy crop. At initial stage, Neem seed kernel extract (NSKE 5%) 50 ml/ltr + Detergent powder@1 gm/ltr can be sprayed as spot application on infested plants. In case of severe infestation, spray Profenophos@500 ml/acre in 150 Ltr of water. Mix one teaspoon washing powder in each 15 Ltr tank Or Quinalphos25 EC@5 ml/liter of water or Chlorpyriphos20 EC @3 ml/liter of water.
<br><br>
<b>White fly:</b>Nymph are of yellow oval while adult are of yellow body covered with a white waxy bloom. They suck the sap from leaves, which lead into poor photosynthesis. It also act as a medium of transferring leaf curl virus disease. In severe infestation defoliation, shedding of boll and poor opening of boll is observed. Developed sooty mould and plant give sick, black appearance.
 <br>Avoid continuous growing of cotton in same field. Adopting crop rotation with non-preferred hosts such as sorghum, ragi, maize etc., Avoid excess vegetative growth for that avoid excess use of nitrogen. Do timely sowing of crop. Keep field clean. Cultivation of most preferred alternate host crops like eggplant, okra, tomato, tobacco and sunflower may be avoided. Install yellow sticky traps for monitoring white fly (2 traps/acre). If infestation white flies is observed then to control, Spray Triazophos @3 ml/Ltr or Thiacloprid @4.5 gm/Ltr water or Acetamiprid@4 gm or 75 WP Acephate@800 gm/200 Ltr water or Imidachloprid@40 ml/acre dissolved in 200 litre of water or Thiamethoxam@40 gm/acre using 200 litre water.
<br><br>
<b>Thrips:</b>Both nymphs and adults lacerate & feed the sap from under surface of leaves. Upper side of leaf turns brown and lower side becomes silvery white.
<br>Seed treatment with imidacloprid 70 WS@7 ml/kg protect the crop from aphids,leaf-hoppers and thrips up to 8 weeks. Spray any one of the insecticides like Methyl demeton 25 EC@160 ml/acre, Buprofezin 25%SC@350 ml/acre, Fipronil 5% SC@200-300 ml/acre, Imidacloprid 70% WG @10-30 ml/acre, Thiamethoxam 25% WG 30 gm/acre in 200 Ltr of water per acre.
<br><br>
<b> Disease and their control:</b><br>
<b>Fusarium wilt:</b>Plants become stunted, yellow, followed by defoliation. Yellowing first occurs around leaf edges and advances inward. Infected plants fruit earlier and produce smaller bolls. It causes a blackening and discoloration just found in a ring just beneath the bark. It affect at all stage of crop.
<br>Use resistant variety to control Fusarium wilt. Avoid continuous planting of cotton in same field. Follow proper crop rotation. Provide good drainage. Treat seed with Trichoderma viride formulation @ 4 gm/kg of seed. To control prepare the solution of Thiophanate Methyl @10 gm and Urea each of 50 gm/10 litre of water and apply near base of plants.
<br><br>
<b>Alternaria Leaf spot:</b>  Small, pale to brown, round or irregular spots of circular to semi-circular size with broad margins appear on leaves. Affected leaves become dry and get fall off. It may causes cankers on stem. Infection is spread to boll, then rotting of boll occurred afterwards they get fall off. Plants stressed by drought, nutrient deficiency and other pests are more susceptible to the disease.
<br>To control this disease, spray Tebuconazole@1 ml/litre or Trifloxystrobin+Tebuconazole@ 0.6 g/litre, on 60th, 90th and 120 days after sowing. If infestation of disease is observed in field, spray with Copper Oxychloride or Captan@500 gm/200 Ltr of water per acre or 12% Carbendazim + 63% WP Mancozeb@25 gm/10 Ltr water.
<br><br>
<b>Cercospora leaf spot:</b>Circular red lesions on leaves which enlarge and turn white or grey in the centre; lesions often have a pattern of concentric rings and possess a red margin. Dark grey spore masses form in the centres of the lesions making them appear dark grey.
 <br>If infestation of disease is observed in field, spray crop with Copper Oxychloride@3 gm/litre or Mancozeb@2.5 gm/ltr, 3 to 4 times in every 15 days interval.
