<button class="accordion"style="width:75%">Soil</button>
<div class="panel">
<p>It can be grown in all type of soil rich in organic content but give best result when grown in well drained loamy or sandy loam soils. It can tolerate pH of range 5.3 to 8.2.</p>
</div>
<br>
<button class="accordion"style="width:75%">Land Preparation</button>
<div class="panel">
<p>Plough the land for two - three times and bring soil to fine tilth. After then carry out planking operation to make land levelled and uniform. Add 10-15ton/acre of well decomposed cow dung at time of last ploughing. For sowing prepared flat beds of 3x2m.</p>
</div><br>
<button class="accordion"style="width:75%">Sowing</button>
<div class="panel">
<p><b>Time of sowing</b><br>
Last week of October and first week of November is best time for sowing.
<br>
<b>Spacing</b><br>
Line to line distance of 22.5 cm is used.<br>
<b>Sowing Depth</b><br>
Sow seeds on bed at depth of 3-4 cm.<br>
<b>Method of sowing</b><br>
For sowing broadcasting method is used.</p>
</div><br>
