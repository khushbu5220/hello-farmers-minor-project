<button class="accordion">Harvesting</button>
<div class="panel">
<p>For vegetable purpose, harvesting of crop can be started from 20-25days after sowing whereas for grain purpose, harvesting is done 90-100days after sowing. For grain purpose, harvest when lower leaves turn yellow and starts shedding and pods turn to yellowish color. Use sickle for harvesting purpose.</p>
</div><br>
<button class="accordion">Post Harvesting</button>
<div class="panel">
<p>After harvesting, tie crops in bundle and allowed to dry in sunlight for 6-7days. After proper drying, carry out threshing after then cleaning and grading operation. </p>
