<p>Fenugreek belongs to leguminous family. In local language it is known as Methi, is the most common vegetable grown throughout the country. Leaves are used for vegetable purpose and its seed are used for flavouring purpose. Leaves and seed also have medicinal value, they help to reduce blood sugar and cholesterol. It is also used for fodder purpose. In India, Rajasthan is major fenugreek growing state. Other major fenugreek producing states are, Madhya Pradesh, Tamil Nadu, Rajasthan, Gujarat, Uttar Pradesh and Punjab.</p><br>
