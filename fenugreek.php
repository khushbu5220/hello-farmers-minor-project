<button class="accordion">Weeding</button>
<div class="panel">
<p>Give one or two hoeings operations to keep field weed free. Take first weeding 25-30days after sowing and second 30days after first weeding. To control weeds chemically, pre-plant application of Fluchloralin@800 ml/acre is recommended or for weed control spray with Pendimethaline@1.3 ltr/acre, by mixing in 200 ltr water within1-2 days of sowing at proper soil moisture.<br>
When plant is about 4 inch height, do pinching operation, it will encourages branching.</p>
</div><br>
<button class="accordion">Irrigation</button>
<div class="panel">
<p>For quick germination of seed, give pre-sowing irrigation. Generally three to four irrigations on 30th day, 75th day, 85th and 105th day after sowing are required for optimum yield of fenugreek crop. Water stress at pod development and seed development stage leads to heavy loss in yield so avoid water stress at this stage.</p>
</div><br>
<button class="accordion">Pesticide and diesease</button>
<div class="panel">
<p><b>Pest and their control:</b><br>
i.<b>Aphid</b>:If Infestation of Aphid is observed, to control take spray of Imidacloprid 3ml/10Ltr water or Thiamethoxam 4gm/10Ltr of water.<br>
<br>
<b> Disease and their control:</b><br>
i.<b>Powdery Mildew</b>: Powdery Mildew: Patchy, white powdery growth appears on upper surface of leaves.<br>
If infestation is observed take spray of water soluble Sulphur 20gm/10Ltr of water. If needed repeat spray with interval of 10days. Moreover take spray of Penconazole10%EC (Topas) 200ml/acre in 200Ltr of water.<br>
ii.<b>Root Rot</b>:To protect crop from root rot, as preventive measure do soil application of Neem Cake 60kg/acre. Also treat seeds with Trichoderma viride@4gm/kg of seed. If infestation of root rot, observed in field to control, drenched soil with Carbendazim 5gm/Ltr of water or Copper oxychloride 2gm/litre of water.</p>
