<div class="panel">
<p><b>Seed Rate:</b>For sowing one acre land, seed rate of 12 kg/acre is used.<br>
<b>Crop Rotation:</b>Fenugreek can be rotated with Kharif crops like Paddy, maize, green gram, fodder crops etc.<br>
<b>Seed Treatment:</b>
Before sowing,soaked seeds in water for 8-12 hr. To protect seeds from soil born pest and diseases, treat them with Thiram@4 gm/kg of seed or Carbendazim 50%WP@3 gm/kg of seed. After chemical treatment, treat seeds with Azospirillum@300 gm/acre + Trichoderma Viride@20 gm/acre for 12 kg of seeds.<br>
