<button class="accordion">Pesticide and diesease</button>
<div class="panel">
<p><b>Pest and their control:</b><br>
<b>Beetles:</b>They eat fresh leaves and make the vines leafless.
<br>Treatment: Spraying of Malathion@400ml per 150ltr water is done to get rid of beetles.
<br>
<br>
<b>Thrips and Jassids:</b>It sucks the juice of leaves and fruits. Jassid sucks the sap from the lower surface of leaves which causes white spots on the upper layer.
<br>Treatment: Spraying of Malathion@400ml per 150ltr water is done to get rid of thrips and jassids.
<br><br>
<b>Leaf roller: </b> Caterpillar makes the leaf roll. It also eats the flowers.<br>Treatment: Spraying of Quinalphos@600ml per 150ltr water is done to get rid of leaf roller.
<br><br>
<b>Yellow and red wasp:</b> This pest eats the mature fruits by doing the hole.
<br>Treatment: Spraying of Quinalphos@600ml per 150ltr water is done to get rid of yellow and red wasp.
<br><br>
<b> Disease and their control:</b><br>
<b>Powdery mildew:</b> Powdery material is seen on both sides of leaves and on a bunch of flowers. Wilting is seen on leaves which will ultimately get dried.
<br>Treatment: Spraying of Carbendazim@400gm or wettable sulphur@600gm is done before flower development and at the time of fruit development.
<br><br>
<b>Downy mildew:</b>Irregular shaped yellow color spots are seen on the upper layer of leaves and on lower layer fungus of white color is seen.
<br>Treatment: The first spray of Mancozeb@400-500gm is done during training and pruning, the second spray is done after 3-4 weeks of the first spray, then the third spray is done before twig development and then fourth spray is done when bunch development start.
<br><br>
<b>Anthracnose:</b>On fruits, stem and branches, deep sunken wounds of canker are seen and on leaves, brown color stains are seen.
<br>Treatment: Spraying of copper oxychloride or M-45@400gm per 150ltr water is done to get rid of anthracnose.
</p></div><br>
