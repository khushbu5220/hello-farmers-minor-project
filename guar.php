<p>Guar is an important legume crop. It is cultivated for fodder as well as for grain purpose. Also it is used as vegetable and green manure crop. It is drought tolerant crop and also thrives in semi-arid regions. Gaur gum is extracted from gaur beans (endosperm of seed) has several industrial uses. It is used to make gum powder which is used in Oil extraction industry, food processing and preservation, printing, textile and paper industry.</p>
