<button class="accordion">Harvesting</button>
<div class="panel">
<p>When it is grown for fodder purpose, harvesting is to be done when crop is at flowering stage. Whereas for green manure purpose, buried crop in field when it is at pod development stage. Depending upon variety used, harvesting for green pods is started from 60-90 days after sowing. Picking for green pods is carried out at interval of 10-12 days. For grain purpose, carry out harvesting once pods gets mature. Cut crop with help of sickle then harvested plants are left in field for a few days for proper sun drying. Afterwards carryout threshing operation.</p>
