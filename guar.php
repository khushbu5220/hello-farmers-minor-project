<button class="accordion">Seeding</button>
<div class="panel">
<p><b>Seed Rate:</b>For sowing use seed rate of 8-10kg/acre.<br>

<b>Seed Treatment:</b>Before sowing, seed treatment is necessary. It will protect seeds from soil borne pest and disease. Dip seeds in hot water@ 56°C for 10minutes then dried seeds at room temperature. Treat seeds with Ceresan or Thiram@3gm/kg of seeds it will protect seeds from fungus. Then dry seeds in shades.    
</p></div>
<br>
<button class="accordion">Planting</button>
<div class="panel">
<p><b>Time of sowing:</b>
Complete sowing from mid-July to mid-August.
<br>

<b>Spacing:</b>
For Sowing use row to row distance of 30 cm.
<br>
 
<b>Sowing Depth:</b>
Sow seeds at depth of 2-3 cm.
<br>

<b>Method of Sowing:</b>
For sowing use seed drill, Pora or kera method.
<br>
</div>
<br>
<button class="accordion">Caring</button>
<div class="panel">
<p><b>Fertilization –</b>Apply Nitrogen@9kg in form of Urea@20kg and Phosphorus@19kg in form of SSP@120kg/acre before sowing.<br><br>
<b>Watering –</b>In rainfed areas, crop did not required irrigation. But if needed provide irrigation depending upon rainfall intensity.
<br><br>
<b>Weeding -</b>Keep field weed free during initial growth of crop by doing hoeing and weeding. To control weed chemically within 24hours of sowing take spray of Pendimethalin@750ml/200Ltr of water per acre.</p>
</div>
<br>
<button class="accordion">Pesticide and diesease</button>
<div class="panel">
<p><b>Pest and their control:</b><br>
<b>Jassid:</b>If infestation is observed, take spray of Malathion 50EC@250-450ml/100Ltr of water for two - three times with interval of 15 days.
<br><br>
<b> Disease and their control:</b><br>
<b>Blight:</b>Dark brown spot with dot like bodies developed on stem, branches, leaflet and pods. In severe infestation, withering of stem and pod is observed.
For cultivation use resistant varieties. On incidence of disease spray with Indofil M-45 or Captan @260gm/100Ltr of water per acre. If necessary repeat the spray at interval of 15 days.
</p></div><br>
