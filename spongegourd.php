<button class="accordion">Irrigation</button>
<div class="panel">
<p>In summer or dry condition, apply irrigation with the interval of 7-10 days and in rainy season, it requires limited irrigation. First irrigation must be done immediately after seed sowing. In total crop requires 7-8 numbers of irrigations.</p>
</div><br>
<button class="accordion">Weed Control</button>
<div class="panel">
<p>To make the field weed free mulching and then weedicides are necessary. Apply Pendimethalin @1 ltr/acre or Fluchloralin @800ml/acre as pre-emergence herbicide.</p>
</div><br>
<button class="accordion"> Plant Protect</button>
<div class ="panel">
<p><b>Pest and their control:</b><br>
i.<b>Beetles:</b> The symptoms are damage to flowers, leaves and stems.<br>
<b>Treatment:</b> Appropriate insecticidal spray will help to cure pests.<br>
ii.<b>Aphid and Thrips:</b> They suck the sap from the leaves resulting in yellowing and drooping of leaves. Thrips results in curling of leaves, leaves become cup shaped or curved upward.<br>
If infestation is observed in field, To control spray the crop with Thiamethoxam@5gm/15Ltr of water.<br>
<br><b>Disease and their control:</b><br><br>
i.<b>Powdery mildew:</b> The symptoms are appearance of white powdery spots on upper surface of leaves which causes leaf withering.<br>
<b>Treatment:</b> Treatment must be given in start of hot and humid summer season. Application of M-45@2gm in 1ltr of water will help to cure powdery mildew. It can also be controlled by fungicidal sprays of Chlorothalonil, benomyl or dinocap.</p>
</div>
