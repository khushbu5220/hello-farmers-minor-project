<button class="accordion" style="width:75%">Soil</button>
<div class="panel">
  <p>It is grown in variety of soil types. It gives best result when grown in sandy loam soil. The pH value of soil ranges from 6.5-7.0 or neutral to slightly alkaline soil is good for plantation.</p>
</div>
<br>
<button class="accordion"style="width:75%">Land Preparation</button>
<div class="panel">
<p>To bring the soil to the fine tilth and to make the field weed free, ploughings are required. At the time of ploughing add Farm Yard Manure (FYM) in the field for the good yield. FYM @84qtl in per acre of land is added for quality crop production.</p>
</div>
<br>
<button class="accordion"style="width:75%">Sowing</button>
<div class="panel">
<p><b>Time of sowing</b><br>
The seeds are sown twice in the year. The ideal time for sowing is in the month of mid-February to March and second time in the month of mid-May to July.<br>
<b>Spacing</b><br>
Sow two seeds per hill which is 3m wide and use spacing of 75-90cm between seeds.<br>
<b>Sowing depth</b><br>
Seeds are sown at the depth of 2.5-3cm.<br>
<b>Method of Sowing</b><br>
Dibbling method is used.</p>
</div>
<br>
