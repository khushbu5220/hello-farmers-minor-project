<button class="accordion">Harvesting</button>
<div class="panel">
<p>Crops are ready for harvesting after 70-80 days of sowing. Carry out picking at the interval of 3-4 days. Harvesting of tender and medium sized fruits should be done. It gives an average yield of 66-83 qtl per acre.</p>
</div>
<br>
<button class="accordion">Seed Production</button>
<div class="panel">
<p>Keep isolation distance of 1000m from other varieties of sponge gourd. Remove diseased plants from the field. The ideal time for growing the seed crop is in the month of February-March because harvesting of seeds will be easier during dry month. For seed production, the fruits are harvested when they get physiologically mature. After harvesting seeds are taken out form pulp, then they are packed and are stored.</p>
</div><br>
