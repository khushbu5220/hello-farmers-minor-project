<div class="panel">
<p><b>Seed Rate</b><br>
Use seed rate of 2.0 kg/acre.<br>
<b>Seed Treatment</b><br>
With the help of filer firstly scrap the seed coat of the seed and then soak the seeds in the water for 24 hours for better viability and higher germination percentage.<br>
</p>
</div>
<br>
<button class="accordion">Nursery Management and Transplanting</button>
<div class="panel">
<center>
<p>Seeds are sown on prepared nursery beds. Avoid seedbeds to touch the ground to protect them from soil-borne diseases. Transplanting is mainly done after 25-30 days of sowing having 4-5 leaves on the seedling.<br>
Seedlings are planted at the distance of 2.5 x 1.2m between rows and plants. Fertilizer application of NPK is applied after 7-10 days of transplanting, then at the time of starting of lateral vine development, then at the flowering period.<br></p>
</div><br>
