<p>It is also known as “Luffa” sponge gourd. The vines of sponge gourd attain the height of 30 feet or more. The fruits of sponge gourd are cylindrical in shape and outer skin is smooth green. The fruit contains white inner flesh which is fibrous and have similar flavor to bitter melon. The fruit attains the height of 1-2 feet. Fully ripened sponge gourd contains high fiber content which is used as cleansing agent and for making table mats, shoe-soles etc. It is a good source of Vitamin A and C. It is used to cure jaundice, diabetes, to purify blood, and is used to cure skin diseases. In India it is grown in Punjab, Bihar, U.P., Delhi, Gujarat, Haryana, Rajasthan and Jharkhand.</p><br>
